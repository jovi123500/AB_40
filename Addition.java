package mypackage;

public class Addition 
{
	static void add()
	{
		int a1=10;
		int a2=20;
		int sum=a1+a2;
		System.out.println(sum);

	}
	static void sub()
	{
		int a1=10;
		int a2=20;
		int s=a2-a1;
		System.out.println(s);

	}
	static void mul()
	{
		int a1=10;
		int a2=20;
		int m=a1*a2;
		System.out.println(m);

	}
	static void div()
	{
		int a1=10;
		int a2=20;
		int d=a2/a1;
		System.out.println(d);

	}
	public static void main(String[] args) 
	{
		add();
		sub();
		mul();
		div();
	}

}
